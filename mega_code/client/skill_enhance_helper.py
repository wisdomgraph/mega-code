def _set_span_attrs(attrs: dict) -> None:
    """Set attributes on the current NDJSON span if available."""
    span = get_current_span()
    for k, v in attrs.items():
        if isinstance(v, (str, int, float, bool)):
            span.set_attribute(k, v)
        else:
            span.set_attribute(k, json.dumps(v, default=str))


_DEFAULT_MAX_SKILLS = 5
_SKILL_IDENTITY_FILENAME = "skill-identity.json"


def _can_update_local_skill_metadata(metadata_path: Path) -> bool:
    """Return whether a local skill metadata.json is eligible for enhancement updates."""
    resolved = metadata_path.resolve()
    return resolved.is_relative_to(PENDING_SKILLS_DIR.resolve()) or resolved.is_relative_to(
        FEEDBACK_DIR.resolve()
    )


def _skill_identity_path(iteration_dir: Path) -> Path:
    """Return the iteration-scoped skill identity artifact path."""
    return iteration_dir / _SKILL_IDENTITY_FILENAME


def _write_skill_identity(
    iteration_dir: Path,
    *,
    original_skill_name: str,
    canonical_skill_name_value: str,
    original_skill_path: Path,
) -> None:
    """Persist original and canonical skill identity for later server upload."""
    payload = {
        "original_skill_name": original_skill_name,
        "canonical_skill_name": canonical_skill_name_value,
        "original_skill_path": str(original_skill_path),
    }
    _skill_identity_path(iteration_dir).write_text(
        json.dumps(payload, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )


def _load_skill_identity(iteration_dir: Path) -> dict:
    """Load the iteration-scoped skill identity artifact if present."""
    identity_path = _skill_identity_path(iteration_dir)
    try:
        data = json.loads(identity_path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError, OSError):
        return {}
    return data if isinstance(data, dict) else {}


def _is_mega_code_skill(author: str) -> bool:
    """Check if a skill was generated by mega-code (case-insensitive)."""
    return MEGACODE_AUTHOR_MARKER in author.lower()


@traced("skill_enhance.scan_skills_dir")
def _scan_skills_dir(
    skills_dir: Path,
    *,
    limit: int | None = None,
    mega_code_only: bool = True,
) -> list[dict]:
    """Scan a Claude skills directory for skills."""
    _set_span_attrs(
        {
            "skill_enhance.scan_dir": str(skills_dir),
            "skill_enhance.scan_dir_exists": skills_dir.exists(),
            "skill_enhance.scan_limit": limit if limit is not None else -1,
            "skill_enhance.scan_mega_code_only": mega_code_only,
        }
    )
    results = []
    skipped_non_mega = 0
    skipped_no_skill_md = 0

    if not skills_dir.exists():
        _set_span_attrs({"skill_enhance.scan_result_count": 0})
        return results

    for skill_dir in sorted(skills_dir.iterdir()):
        if limit is not None and len(results) >= limit:
            break
        if not skill_dir.is_dir():
            continue
        skill_md = skill_dir / "SKILL.md"
        if not skill_md.exists():
            skipped_no_skill_md += 1
            continue

        content = skill_md.read_text(encoding="utf-8")
        fm = parse_frontmatter(content)
        author = str(skill_frontmatter_value(fm, "author", ""))
        if mega_code_only and not _is_mega_code_skill(author):
            skipped_non_mega += 1
            continue

        name = canonical_skill_name(skill_dir.name, content)
        results.append(
            {
                "name": name,
                "description": str(fm.get("description", "")),
                "state": "installed",
                "path": str(skill_md),
            }
        )

    _set_span_attrs(
        {
            "skill_enhance.scan_result_count": len(results),
            "skill_enhance.scan_skipped_non_mega": skipped_non_mega,
            "skill_enhance.scan_skipped_no_skill_md": skipped_no_skill_md,
            "skill_enhance.scan_result_names": json.dumps([r["name"] for r in results]),
        }
    )
    return results


def _project_skills_dir() -> Path:
    """Return the project-scoped Claude skills directory."""
    project_dir = _resolve_current_project_dir()
    if project_dir is None:
        project_dir = Path(os.getcwd()).resolve()
    return project_dir / ".agents" / "skills"


def _user_skills_dir() -> Path:
    """Return the user-scoped Claude skills directory."""
    return Path.home() / ".agents" / "skills"


def _current_project_id() -> str:
    """Return the current MEGA-Code project id.

    Uses ``_resolve_current_project_dir`` (session-first, then env/cwd) and
    converts via ``get_project_folder_name`` (``<basename>_<hash[:8]>``).
    """
    if resolved := _resolve_current_project_dir():
        return get_project_folder_name(str(resolved))
    return ""


def _mapped_project_dir_from_session(session_id: str) -> Path | None:
    """Resolve the real project directory for a Claude session via mapping.json."""
    session_dir = find_session_dir(session_id)
    if session_dir is None:
        return None

    mapped_project_dir = load_mapping().get(session_dir.parent.name)
    if not mapped_project_dir:
        return None

    resolved = Path(mapped_project_dir).resolve()
    return resolved if resolved.exists() else None


def _project_dir_from_session() -> Path | None:
    """Return the real project directory for the active Codex session."""
    session_id = os.environ.get("CODEX_THREAD_ID", "").strip()
    if not session_id:
        return None

    return _mapped_project_dir_from_session(session_id)


def _apply_cli_project_dir(project_dir: str) -> None:
    """Apply an explicit project directory override for this command invocation."""
    candidate = project_dir.strip()
    if not candidate:
        return
    resolved = Path(candidate).resolve()
    if resolved.exists():
        os.environ["CODEX_PROJECT_DIR"] = str(resolved)


def _resolve_current_project_dir() -> Path | None:
    """Resolve the user's real project directory for skill discovery.

    Resolution order:
    1. Session-backed project mapping via ``CODEX_THREAD_ID``
    2. ``CODEX_PROJECT_DIR``
    3. Current working directory
    """
    if session_project_dir := _project_dir_from_session():
        return session_project_dir

    env_project_dir = os.environ.get("CODEX_PROJECT_DIR", "").strip()
    if env_project_dir:
        resolved = Path(env_project_dir).resolve()
        if resolved.exists():
            return resolved

    cwd = Path(os.getcwd()).resolve()
    if cwd.exists():
        return cwd

    return None


