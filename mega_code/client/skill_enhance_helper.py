def _set_span_attrs(attrs: dict) -> None:
    """Set attributes on the current span if available."""
    try:
        from opentelemetry import trace

        span = trace.get_current_span()
    except ImportError:
        return
    for k, v in attrs.items():
        if isinstance(v, (str, int, float, bool)):
            span.set_attribute(k, v)
        else:
            span.set_attribute(k, json.dumps(v, default=str))


_AUTHOR_MATCH = MEGACODE_AUTHOR_MARKER
_DEFAULT_MAX_SKILLS = 5
_SKILL_IDENTITY_FILENAME = "skill-identity.json"


def _load_pending_skill_metadata(skill_name: str) -> dict:
    """Load local pending-skill metadata.json if present."""
    metadata_path = PENDING_SKILLS_DIR / skill_name / "metadata.json"
    if not metadata_path.resolve().is_relative_to(PENDING_SKILLS_DIR.resolve()):
        return {}
    if not metadata_path.exists():
        return {}
    try:
        data = json.loads(metadata_path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {}
    return data if isinstance(data, dict) else {}


def _skill_identity_path(iteration_dir: Path) -> Path:
    """Return the iteration-scoped skill identity artifact path."""
    return iteration_dir / _SKILL_IDENTITY_FILENAME


def _write_skill_identity(
    iteration_dir: Path,
    *,
    original_skill_name: str,
    canonical_skill_name_value: str,
    original_skill_path: Path,
) -> None:
    """Persist original and canonical skill identity for later server upload."""
    payload = {
        "original_skill_name": original_skill_name,
        "canonical_skill_name": canonical_skill_name_value,
        "original_skill_path": str(original_skill_path),
    }
    _skill_identity_path(iteration_dir).write_text(
        json.dumps(payload, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )


def _load_skill_identity(iteration_dir: Path) -> dict:
    """Load the iteration-scoped skill identity artifact if present."""
    identity_path = _skill_identity_path(iteration_dir)
    try:
        data = json.loads(identity_path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError, OSError):
        return {}
    return data if isinstance(data, dict) else {}


