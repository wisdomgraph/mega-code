def _infer_project_id_from_metadata(skill_name: str, meta: dict | None = None) -> str:
    """Try to extract project_id from the skill's local metadata.json.

    Parses the ``output_files`` paths which have the form
    ``pg://skill_artifacts/<project_id>/...``.
    """
    if meta is None:
        meta = _load_pending_skill_metadata(skill_name)
    for output_file in meta.get("output_files", []):
        # Format: pg://skill_artifacts/<project_id>/...
        parts = output_file.split("/")
        if len(parts) >= 4 and parts[0] == "pg:" and parts[2] == "skill_artifacts":
            return parts[3]
    return ""


def _infer_run_id_from_metadata(skill_name: str, meta: dict | None = None) -> str:
    """Extract run_id from the skill's local metadata.json."""
    if meta is None:
        meta = _load_pending_skill_metadata(skill_name)
    run_id = meta.get("run_id", "")
    return str(run_id) if run_id else ""


@traced("skill_enhance.accept_enhanced_skill")
def accept_enhanced_skill(
    original_skill_path: Path,
    enhanced_skill_path: Path,
    iteration_dir: Path,
    iteration: int,
    eval_roi: dict | None = None,
) -> Path:
    """Back up original skill to iteration dir, replace with enhanced version.

    1. Copy original SKILL.md -> iteration_dir/original-skill.md
    2. Bump semantic version in enhanced skill frontmatter
    3. Refresh generated_at in enhanced skill frontmatter
    4. Inject only the new eval ROI into frontmatter if provided
    5. Replace original SKILL.md with enhanced content

    Args:
        original_skill_path: Path to the original SKILL.md file.
        enhanced_skill_path: Path to the enhanced skill written by host agent.
        iteration_dir: Path to the iteration-N directory.
        iteration: Current iteration number.
        eval_roi: Optional dict with eval ROI data to inject into frontmatter.
            Expected keys: ``model``, ``performance_increase``, ``token_savings``,
            ``test_count``, ``with_success_rate``, ``baseline_success_rate``.

    Returns:
        Path to the replaced SKILL.md.
    """
    import shutil

    _set_span_attrs(
        {
            "skill_enhance.original_path": str(original_skill_path),
            "skill_enhance.enhanced_path": str(enhanced_skill_path),
            "skill_enhance.iteration": iteration,
        }
    )

    # 1. Back up original
    backup_path = iteration_dir / "original-skill.md"
    shutil.copy2(original_skill_path, backup_path)
    logger.info("Backed up original skill to %s", backup_path)

    # 2. Read both files and normalize frontmatter
    original_content = original_skill_path.read_text(encoding="utf-8")
    enhanced_content = enhanced_skill_path.read_text(encoding="utf-8")

    original_fm, _ = split_frontmatter(original_content)
    enhanced_fm, enhanced_body = split_frontmatter(enhanced_content)

    original_version = str(skill_frontmatter_value(original_fm, "version", "1.0.0"))
    original_author = skill_frontmatter_value(original_fm, "author", "")
    original_tags = skill_frontmatter_value(original_fm, "tags", [])
    next_version = bump_minor_version(original_version)
    refreshed_generated_at = current_timestamp_z()

    normalized_frontmatter = normalize_skill_frontmatter(enhanced_fm)
    metadata = dict(normalized_frontmatter.get("metadata", {}))

    if original_version:
        metadata["version"] = next_version
    if original_author and "author" not in metadata:
        metadata["author"] = str(original_author)
    if isinstance(original_tags, list) and original_tags and "tags" not in metadata:
        metadata["tags"] = original_tags
    metadata["generated_at"] = refreshed_generated_at

    metadata.pop("eval_version", None)
    metadata.pop("enhanced_from", None)
    metadata.pop("roi", None)

    if eval_roi:
        metadata["roi"] = [format_eval_roi_entry(eval_roi)]

    normalized_frontmatter["metadata"] = metadata
    enhanced_content = render_frontmatter(normalized_frontmatter) + enhanced_body

    final_skill_dir = original_skill_path.parent
    original_skill_name = final_skill_dir.name
    final_skill_name = canonical_skill_name(final_skill_dir.name, enhanced_content)
    _write_skill_identity(
        iteration_dir,
        original_skill_name=original_skill_name,
        canonical_skill_name_value=final_skill_name,
        original_skill_path=original_skill_path,
    )
    if (
        final_skill_name != final_skill_dir.name
        and final_skill_dir.resolve().parent == PENDING_SKILLS_DIR.resolve()
    ):
        renamed_dir = final_skill_dir.with_name(final_skill_name)
        if renamed_dir.exists():
            logger.warning(
                "Skipping skill directory rename from %s to %s because target exists",
                final_skill_dir,
                renamed_dir,
            )
        else:
            final_skill_dir.rename(renamed_dir)
            final_skill_dir = renamed_dir

    final_skill_path = final_skill_dir / original_skill_path.name

    # 3. Replace original with enhanced
    final_skill_path.write_text(enhanced_content, encoding="utf-8")
    logger.info(
        "Replaced %s with enhanced version (v%s -> v%s)",
        final_skill_path,
        original_version,
        next_version,
    )

    # 4. Update local metadata.json ROI if it exists
    if eval_roi:
        metadata_path = final_skill_dir / "metadata.json"
        if (
            metadata_path.resolve().is_relative_to(PENDING_SKILLS_DIR.resolve())
            and metadata_path.exists()
        ):
            try:
                meta = json.loads(metadata_path.read_text(encoding="utf-8"))
                roi_entry = format_eval_roi_entry(eval_roi, include_analytics=True)

                existing_roi = meta.get("roi", [])
                if not isinstance(existing_roi, list):
                    existing_roi = []
                existing_roi.append(roi_entry)
                meta["roi"] = existing_roi
                metadata_path.write_text(
                    json.dumps(meta, indent=2, ensure_ascii=False), encoding="utf-8"
                )
                logger.info("Updated local metadata.json ROI for '%s'", final_skill_dir.name)
            except (json.JSONDecodeError, OSError) as e:
                logger.warning("Failed to update local metadata.json: %s", e)
        elif not metadata_path.resolve().is_relative_to(PENDING_SKILLS_DIR.resolve()):
            logger.warning("Refusing path traversal in skill dir: %s", final_skill_dir)

    return final_skill_path


@traced("skill_enhance.store_enhanced_skill_on_server")
def store_enhanced_skill_on_server(
    skill_name: str,
    enhanced_content: str,
    iteration: int,
    eval_roi: dict | None = None,
    run_id: str | None = None,
    iteration_dir: Path | None = None,
) -> None:
    """Store enhanced skill on server as a new versioned entry.

    Args:
        skill_name: Original skill name.
        enhanced_content: Enhanced SKILL.md content.
        iteration: Current iteration number.
        eval_roi: Optional eval ROI data to include in metadata.
    """
    canonical_name = canonical_skill_name(skill_name, enhanced_content)
    frontmatter, _ = split_frontmatter(enhanced_content)
    version = str(skill_frontmatter_value(frontmatter, "version", "")) or f"{iteration + 1}.0.0"
    parent_skill_name = skill_name
    metadata_lookup_name = skill_name

    if iteration_dir is not None:
        identity = _load_skill_identity(iteration_dir)
        original_skill_name = identity.get("original_skill_name")
        if isinstance(original_skill_name, str) and original_skill_name.strip():
            parent_skill_name = original_skill_name
            metadata_lookup_name = original_skill_name

    _set_span_attrs(
        {
            "skill_enhance.skill_name": canonical_name,
            "skill_enhance.parent_skill_name": parent_skill_name,
            "skill_enhance.version": version,
            "skill_enhance.iteration": iteration,
        }
    )

    mode = os.environ.get("MEGA_CODE_CLIENT_MODE", "local")
    if mode != "remote":
        logger.info("Local mode — server update skipped (MEGA_CODE_CLIENT_MODE=%r)", mode)
        return

    author = str(skill_frontmatter_value(frontmatter, "author", ""))
    if not _is_mega_code_skill(author):
        logger.info("Non-mega-code skill author detected — server update skipped")
        return

    meta = _load_pending_skill_metadata(metadata_lookup_name)
    project_id = _infer_project_id_from_metadata(metadata_lookup_name, meta)
    if not project_id and metadata_lookup_name != canonical_name:
        meta = _load_pending_skill_metadata(canonical_name)
        project_id = _infer_project_id_from_metadata(canonical_name, meta)
        metadata_lookup_name = canonical_name
    if not project_id:
        logger.warning("Cannot determine project_id from metadata, skipping server update")
        return
    resolved_run_id = run_id or _infer_run_id_from_metadata(metadata_lookup_name, meta)

    # Build metadata with full ROI (includes test_count, success rates for analytics)
    metadata = None
    if eval_roi:
        roi_entry = format_eval_roi_entry(eval_roi, include_analytics=True)
        metadata = {"roi": [roi_entry]}

    try:
        from mega_code.client.api import create_client

        client = create_client(mode="remote")
        enhance_kwargs = {
            "skill_name": canonical_name,
            "project_id": project_id,
            "skill_md": enhanced_content,
            "version": version,
            "parent_skill_name": parent_skill_name,
            "metadata": metadata,
        }
        if resolved_run_id:
            enhance_kwargs["run_id"] = resolved_run_id

        result = client.enhance_skill(
            **enhance_kwargs,
        )
        if result.success:
            logger.info("Server version created: '%s' v%s", canonical_name, version)
        else:
            logger.warning("Server enhance failed: %s", result.message)
    except (ValueError, OSError, ConnectionError) as e:
        logger.warning("Failed to store enhanced skill on server: %s", e)


