def _is_mega_code_skill(author: str) -> bool:
    """Check if a skill was generated by mega-code (case-insensitive)."""
    return _AUTHOR_MATCH in author.lower()


def _scan_skills_dir(
    skills_dir: Path,
    *,
    limit: int | None = None,
    mega_code_only: bool = True,
) -> list[dict]:
    """Scan a Claude skills directory for skills."""
    results = []

    if not skills_dir.exists():
        return results

    for skill_dir in sorted(skills_dir.iterdir()):
        if limit is not None and len(results) >= limit:
            break
        if not skill_dir.is_dir():
            continue
        skill_md = skill_dir / "SKILL.md"
        if not skill_md.exists():
            continue

        content = skill_md.read_text(encoding="utf-8")
        fm = parse_frontmatter(content)
        author = str(skill_frontmatter_value(fm, "author", ""))
        if mega_code_only and not _is_mega_code_skill(author):
            continue

        results.append(
            {
                "name": canonical_skill_name(skill_dir.name, content),
                "description": str(fm.get("description", "")),
                "state": "installed",
                "path": str(skill_md),
            }
        )

    return results


def _project_skills_dir() -> Path:
    """Return the project-scoped Claude skills directory."""
    project_dir = Path(os.environ.get("CLAUDE_PROJECT_DIR", os.getcwd())).resolve()
    return project_dir / ".claude" / "skills"


def _user_skills_dir() -> Path:
    """Return the user-scoped Claude skills directory."""
    return Path.home() / ".claude" / "skills"


def _current_project_id() -> str:
    """Return a project id derived from CLAUDE_PROJECT_DIR.

    Uses ``<basename>-<hash[:8]>`` so that directories with the same
    basename under different parents (e.g. ~/alpha/myapp vs ~/beta/myapp)
    do not collide.
    """
    resolved = Path(os.environ.get("CLAUDE_PROJECT_DIR", os.getcwd())).resolve()
    short_hash = hashlib.sha256(str(resolved).encode()).hexdigest()[:8]
    return f"{resolved.name}-{short_hash}"


def _scan_project_installed_skills(
    *,
    limit: int | None = None,
    mega_code_only: bool = True,
) -> list[dict]:
    """Scan project-scoped installed skills."""
    return _scan_skills_dir(_project_skills_dir(), limit=limit, mega_code_only=mega_code_only)


def _scan_user_installed_skills(
    *,
    limit: int | None = None,
    mega_code_only: bool = True,
) -> list[dict]:
    """Scan user-scoped installed skills."""
    return _scan_skills_dir(_user_skills_dir(), limit=limit, mega_code_only=mega_code_only)


def _scan_archived_skills(
    *,
    project_id: str,
    limit: int | None = None,
    mega_code_only: bool = True,
) -> list[dict]:
    """Scan archived skills for the current project, most recent run first."""
    results = []
    project_dir = FEEDBACK_DIR / project_id
    if not project_id or not project_dir.exists():
        return results

    archived_runs = []
    for run_dir in project_dir.iterdir():
        if not run_dir.is_dir():
            continue
        manifest_path = run_dir / "manifest.json"
        if not manifest_path.exists():
            continue
        try:
            manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue
        archived_runs.append((str(manifest.get("archived_at", "")), run_dir))

    for _, run_dir in sorted(archived_runs, reverse=True):
        if limit is not None and len(results) >= limit:
            break
        skills_dir = run_dir / "skills"
        if not skills_dir.exists():
            continue
        for skill_dir in sorted(skills_dir.iterdir()):
            if limit is not None and len(results) >= limit:
                break
            if not skill_dir.is_dir():
                continue
            skill_md = skill_dir / "SKILL.md"
            if not skill_md.exists():
                continue

            content = skill_md.read_text(encoding="utf-8")
            fm = parse_frontmatter(content)
            author = str(skill_frontmatter_value(fm, "author", ""))
            if mega_code_only and not _is_mega_code_skill(author):
                continue

            results.append(
                {
                    "name": canonical_skill_name(skill_dir.name, content),
                    "description": str(fm.get("description", "")),
                    "state": "archived",
                    "path": str(skill_md),
                }
            )

    return results


@traced("skill_enhance.list_skills")
def list_skills(max_skills: int = _DEFAULT_MAX_SKILLS) -> list[dict]:
    """List mega-code skills from project, user, pending, and archived paths.

    Returns list of {name, description, state, path} dicts,
    capped at *max_skills* (default 5).
    """
    seen = set()
    deduped = []

    def add_unique(skills: Iterable[dict]) -> bool:
        for skill in skills:
            if skill["name"] in seen:
                continue
            seen.add(skill["name"])
            deduped.append(skill)
            if len(deduped) >= max_skills:
                return True
        return False

    if add_unique(_scan_project_installed_skills(limit=max_skills, mega_code_only=True)):
        _set_span_attrs(
            {
                "skill_enhance.skill_count": len(deduped),
                "skill_enhance.skill_names": [s["name"] for s in deduped],
            }
        )
        return deduped

    remaining = max_skills - len(deduped)
    if add_unique(_scan_user_installed_skills(limit=remaining, mega_code_only=True)):
        _set_span_attrs(
            {
                "skill_enhance.skill_count": len(deduped),
                "skill_enhance.skill_names": [s["name"] for s in deduped],
            }
        )
        return deduped

    remaining = max_skills - len(deduped)
    pending_results = []
    for skill_info in get_pending_skills():
        if len(pending_results) >= remaining:
            break
        if _is_mega_code_skill(skill_info.author):
            pending_results.append(
                {
                    "name": skill_info.name,
                    "description": skill_info.description,
                    "state": "pending",
                    "path": skill_info.path,
                }
            )
    if add_unique(pending_results):
        _set_span_attrs(
            {
                "skill_enhance.skill_count": len(deduped),
                "skill_enhance.skill_names": [s["name"] for s in deduped],
            }
        )
        return deduped

    remaining = max_skills - len(deduped)
    add_unique(
        _scan_archived_skills(
            project_id=_current_project_id(),
            limit=remaining,
            mega_code_only=True,
        )
    )

    _set_span_attrs(
        {
            "skill_enhance.skill_count": len(deduped),
            "skill_enhance.skill_names": [s["name"] for s in deduped],
        }
    )
    return deduped


